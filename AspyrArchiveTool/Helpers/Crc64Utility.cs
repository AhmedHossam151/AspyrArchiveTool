        public static ulong CalculateBlockCrc(byte[] data, int length)
        {
            ulong crc = 0xFFFFFFFFFFFFFFFFUL;
            for (int i = 0; i < length; i++)
            {
                byte index = (byte)((crc ^ data[i]) & 0xFF);
                crc = (crc >> 8) ^ CrcTable[index];
            }
            return crc;
        }

        public static List<ulong> CalculateCrcForFile(string filePath)
        {
            var crcs = new List<ulong>();
            using var fs = new FileStream(filePath, FileMode.Open, FileAccess.Read);
            byte[] buffer = new byte[CrcBlockSize];
            int bytesRead;

            while ((bytesRead = fs.Read(buffer, 0, buffer.Length)) > 0)
            {
                crcs.Add(CalculateBlockCrc(buffer, bytesRead));
            }

            return crcs;
        }

        public static void GenerateCrcFile(string obbPath, string crcPath)
        {
            var crcs = CalculateCrcForFile(obbPath);
            using var writer = new BinaryWriter(File.OpenWrite(crcPath));
            foreach (var crc in crcs)
            {
                writer.Write(crc);
            }
        }
